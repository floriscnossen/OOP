
public class Product {
	private String name;
	private int costs;
	
	public Product(String name, int costs) {
		super();
		this.name = name;
		this.costs = costs;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCosts() {
		return costs;
	}
	public void setCosts(int costs) {
		this.costs = costs;
	}
	
	
}
